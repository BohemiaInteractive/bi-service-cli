module.exports = Config;


/**
 * Config
 *
 */
function Config() {
    this.stores = {
        literal: {
            store: {}
        }
    };
}


/**
 * get
 *
 * @param {String} key
 *
 * @return {undefined}
 */
Config.prototype.get = function(key) {
    return this.stores.literal.store[key];
};

